#ifdef ITALIAN_RULES
#define DB_PATH "c:/db_italian/wld_v2"
#else
#define DB_PATH "c:/db_english/wld_v2"
#endif

struct TEST_POSITION {
	char *fen;
	int npieces;
	int color;
	int value;
	EGDB_BITBOARD pos;
};


extern TEST_POSITION positions[];
extern const int num_positions;


void print_msgs(char *msg)
{
	printf("%s", msg);
}


int main()
{
	int i, status, max_pieces, value, nerrors;
	EGDB_TYPE egdb_type;
	EGDB_DRIVER *handle;

	/* Check that db files are present, get db type and size. */
	status = egdb_identify(DB_PATH, &egdb_type, &max_pieces);
	if (status) {
		printf("No database found at %s\n", DB_PATH);
		return(1);
	}
	printf("Database type %d found with max pieces %d\n", egdb_type, max_pieces);

	/* Open database for probing. */
	handle = egdb_open(EGDB_NORMAL, max_pieces, 2000, DB_PATH, print_msgs);
	if (!handle) {
		printf("Error returned from egdb_open()\n");
		return(1);
	}

	/* Lookup each test position, verify the WLD value. */
	nerrors = 0;
	printf("\nTesting positions.\n");
	for (i = 0; i < num_positions; ++i) {
		if (positions[i].npieces > max_pieces)
			break;

		value = handle->lookup(handle, &positions[i].pos, positions[i].color, 0);
		if (value != positions[i].value) {
			printf("%d: lookup error, expected %d, got %d\n", i, value, positions[i].value);
			++nerrors;
		}
	}
	printf("Tested %d positions, %d errors.\n", i, nerrors);

	handle->close(handle);
    return(0);
}


